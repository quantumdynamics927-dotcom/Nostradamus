# === AGGREGATE STATISTICS ===

def get_disaster_summary() -> Dict:
    """Return summary statistics for disaster events."""
    from collections import Counter
    types = Counter(e["event_type"] for e in DISASTERS_KB)
    years = [e["start_year"] for e in DISASTERS_KB]
    return {
        "total": len(DISASTERS_KB),
        "by_type": dict(types),
        "year_range": (min(years), max(years)),
        "total_casualties": sum(e.get("casualties", 0) for e in DISASTERS_KB),
    }

def get_space_summary() -> Dict:
    """Return summary statistics for space events."""
    from collections import Counter
    types = Counter(e["event_type"] for e in SPACE_EVENTS_KB)
    years = [e["start_year"] for e in SPACE_EVENTS_KB]
    return {
        "total": len(SPACE_EVENTS_KB),
        "by_type": dict(types),
        "year_range": (min(years), max(years)),
    }
